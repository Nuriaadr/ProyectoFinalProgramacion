    private void EditarPartidoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditarPartidoActionPerformed
        try {
            if (partidoActual != null) {
                SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");

                Date nuevaFecha = sdf.parse(fechaEditar.getText());
                String nuevoLugar = lugarEditar.getText();
                Arbitro nuevoArbitro = (Arbitro) Arbitros.getSelectedItem();

                partidoActual.setFecha(nuevaFecha);
                partidoActual.setLugar(nuevoLugar);
                partidoActual.setArbitro(nuevoArbitro);

                partDAO.actualizar(partidoActual);

                JOptionPane.showMessageDialog(this, "Partido actualizado correctamente.");
            } else {
                JOptionPane.showMessageDialog(this, "Primero busca un partido para editar.");
            }
        } catch (ParseException e) {
            JOptionPane.showMessageDialog(this, "Formato de fecha incorrecto. Usa yyyy-MM-dd.");
        } catch (HeadlessException e) {
            JOptionPane.showMessageDialog(this, "Error al actualizar el partido.");
            e.printStackTrace(); 
        }
    }//GEN-LAST:event_EditarPartidoActionPerformed

