    private void BuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BuscarActionPerformed
        // TODO add your handling code here:   
        try {
            int id = Integer.parseInt(idBuscar.getText());
            partidoActual = partDAO.leer(id);

            if (partidoActual != null) {
                SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                fechaEditar.setText(sdf.format(partidoActual.getFecha()));

                lugarEditar.setText(partidoActual.getLugar());
                Arbitros.setSelectedItem(partidoActual.getArbitro());

            } else {
                JOptionPane.showMessageDialog(this, "Partido no encontrado.");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "ID inv√°lido.");
        }
    }//GEN-LAST:event_BuscarActionPerformed

