    private void CrearParticipacionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CrearParticipacionActionPerformed
        try {
            Jugador jugador = (Jugador) JugadorParticipa.getSelectedItem();
            Partido partido = (Partido) PartidoParticipa.getSelectedItem();
            int goles = (Integer) Goles.getValue();

            if (jugador == null || partido == null) {
                JOptionPane.showMessageDialog(this, "Selecciona un jugador y un partido.");
                return;
            }

            Participacion participacion = new Participacion();
            participacion.setJugador(jugador);
            participacion.setPartido(partido);
            participacion.setGoles(goles);
            if (goles < 0) {
                JOptionPane.showMessageDialog(this, "Los goles no pueden ser negativos.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            ParticipacionDAO participacionDAO = new ParticipacionDAO();
            participacionDAO.crear(participacion);

            JOptionPane.showMessageDialog(this, "Participaci贸n creada correctamente.");
            dispose();

        } catch (HeadlessException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al crear participaci贸n: " + e.getMessage());
        }
    }//GEN-LAST:event_CrearParticipacionActionPerformed

