    private void editarPartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarPartActionPerformed
        if (participacionActual != null) {

            Jugador jugadorSeleccionado = (Jugador) JugadorParticipa.getSelectedItem();
            Partido partidoSeleccionado = (Partido) PartidoParticipa.getSelectedItem();
            int golesMarcados = (int) Goles.getValue();

            
            participacionActual.setJugador(jugadorSeleccionado);
            participacionActual.setPartido(partidoSeleccionado);
           
            participacionActual.setGoles(golesMarcados);

            if (golesMarcados < 0) {
            JOptionPane.showMessageDialog(this, "Los goles no pueden ser negativos.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
            
            
            participacionDAO.actualizar(participacionActual);
            JOptionPane.showMessageDialog(this, "Participación actualizada correctamente.");

        } else {
            JOptionPane.showMessageDialog(this, "Debe buscar una participación para editar primero.");

        }
    }//GEN-LAST:event_editarPartActionPerformed

