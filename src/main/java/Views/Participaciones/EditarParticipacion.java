    private void buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarActionPerformed

        try {
            int participacionId = Integer.parseInt(id.getText().trim());

            ParticipacionDAO participacionDAO = new ParticipacionDAO();
            Participacion participacion = participacionDAO.leer(participacionId);

            if (participacion == null) {
                JOptionPane.showMessageDialog(this, "No se encontró ninguna participación con ese ID.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            for (int i = 0; i < JugadorParticipa.getItemCount(); i++) {
                if (JugadorParticipa.getItemAt(i).getId() == participacion.getJugador().getId()) {
                    JugadorParticipa.setSelectedIndex(i);
                    break;
                }
            }

            for (int i = 0; i < PartidoParticipa.getItemCount(); i++) {
                if (PartidoParticipa.getItemAt(i).getId() == participacion.getPartido().getId()) {
                    PartidoParticipa.setSelectedIndex(i);
                    break;
                }
            }
            Goles.setValue(participacion.getGoles());

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Introduce un ID válido.", "Error", JOptionPane.ERROR_MESSAGE);
        } catch (HeadlessException e) {
            JOptionPane.showMessageDialog(this, "Error al buscar la participación: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_buscarActionPerformed

